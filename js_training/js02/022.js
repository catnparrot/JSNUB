//비교연산자
console.log("5 == 5 :", 5 == 5);
console.log("\"5\" == 5 :", "5" == 5);
console.log("5 == '5' :", 5 == '5');
console.log("5 != 1 :", 5 != 1);
console.log("5 != \"1\" :", 5 != "1");
console.log("5 != \"5\" :", 5 != "5");
console.log("5 === 5 :", 5 === 5);
console.log("5 === \"5\" :", 5 === "5");
console.log("5 !== 10 :", 5 !== 10);
console.log("5 !== \"5\" :", 5 !== "5");

// ==(동등 연산자), !=(부등연산자)는 비교 대상값의 자료형이
// 서로 다르면 강제로 형을 바꾸어 비교한다.