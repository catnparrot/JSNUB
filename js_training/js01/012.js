// 조건문
let subject = '자바스크립트';
switch (subject) {
    case 'C언어':
        console.log('초보자를 위한 C++');
        break;

    case '자바스크립트':
        console.log('초보자를 위한 자바스크립트');
        break;
        
    case '파이썬':
        console.log('초보자를 위한 파이썬');
        break;

    default:
        console.log('초보자 아님')
        break;
}