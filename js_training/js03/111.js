// 특정 자릿수에서 올림하기(ceil)
const posNum = 93.54;
const negNum = -39.27;

console.log(Math.ceil(posNum));
console.log(Math.ceil(negNum));
console.log(Math.ceil(posNum * 10) / 10);
console.log(Math.ceil(posNum / 10) * 10);
console.log(Math.ceil(negNum * 10) / 10);
console.log(Math.ceil(negNum / 10) * 10);
