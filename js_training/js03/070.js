//문자열 자르기 3(substr)
const sentence = 'Wakanda Forever!!!';
console.log("8: ", sentence.substr(8));
console.log("8, 7: ", sentence.substr(8, 7));
console.log("0: ", sentence.substr(0));
console.log("-10: ", sentence.substr(-10));
console.log("0, -3: ", sentence.substr(0, -3));
console.log("30: ", sentence.substr(30));
console.log("0, 30: ", sentence.substr(0, 30));