// 특정 자릿수에서 내림하기(floor)
const posNum = 93.54;
const negNum = -39.27;

console.log(Math.floor(posNum));
console.log(Math.floor(negNum));
console.log(Math.floor(posNum * 10) / 10);
console.log(Math.floor(posNum / 10) * 10);
console.log(Math.floor(negNum * 10) / 10);
console.log(Math.floor(negNum / 10) * 10);
